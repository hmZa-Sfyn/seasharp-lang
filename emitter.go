func (e *Emitter) paramsStr(params []*Param) string {
	parts := make([]string, len(params))
	for i, p := range params {
		prefix := ""
		if p.Modifier.Lexeme == "ref" || p.Modifier.Lexeme == "out" {
			prefix = "&"
		} else if p.Modifier.Lexeme == "params" {
			// variadic — use vector
			parts[i] = fmt.Sprintf("std::vector<%s> %s", e.typeNodeStr(p.Type), p.Name.Lexeme)
			continue
		}
		parts[i] = fmt.Sprintf("%s%s %s", e.typeNodeStr(p.Type), prefix, p.Name.Lexeme)
		if p.Default != nil {
			parts[i] += " = " + e.exprStr(p.Default)
		}
	}
	return strings.Join(parts, ", ")
}

