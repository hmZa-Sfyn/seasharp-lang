// ── Type node → C++ type string ───────────────────────────────────────────────

func (e *Emitter) typeNodeStr(tn *TypeNode) string {
	if tn == nil {
		return "void"
	}
	base := e.mapCsType(tn.Tok.Lexeme)
	if len(tn.Generic) > 0 {
		args := make([]string, len(tn.Generic))
		for i, g := range tn.Generic {
			args[i] = e.typeNodeStr(g)
		}
		switch tn.Tok.Lexeme {
		case "List":
			return fmt.Sprintf("std::vector<%s>", strings.Join(args, ", "))
		case "Dictionary":
			return fmt.Sprintf("std::unordered_map<%s>", strings.Join(args, ", "))
		case "Queue":
			return fmt.Sprintf("std::queue<%s>", strings.Join(args, ", "))
		case "Stack":
			return fmt.Sprintf("std::stack<%s>", strings.Join(args, ", "))
		case "HashSet":
			return fmt.Sprintf("std::unordered_set<%s>", strings.Join(args, ", "))
		case "IEnumerable", "IList", "ICollection":
			return fmt.Sprintf("std::vector<%s>", strings.Join(args, ", "))
		default:
			return fmt.Sprintf("%s<%s>", base, strings.Join(args, ", "))
		}
	}
	if tn.IsArray {
		return fmt.Sprintf("std::vector<%s>", base)
	}
	if tn.IsNullable {
		// Use pointer for nullable value types
		return fmt.Sprintf("%s*", base)
	}
	return base
}

